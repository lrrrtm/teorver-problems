# Ввод значений P1 и P2
P1 = float(input("Введите вероятность сдачи первого экзамена (P1): "))
P2 = float(input("Введите вероятность сдачи второго экзамена (P2): "))

# Вероятность не сдать первый экзамен
P1_not = 1 - P1

# Вероятность не сдать второй экзамен
P2_not = 1 - P2

# Вероятность сдать оба экзамена
P_both = P1 * P2

# Вероятность не сдать ни один из экзаменов
P_neither = P1_not * P2_not

# Вероятность вылета на допсессию (не сдать хотя бы один экзамен)
P_A = 1 - P_both

# Вероятность не сдать только первый экзамен, а второй сдать
P_B_given_A = (P1_not * P2) / P_A

# Результаты
print(f"Вероятность не сдать хотя бы один экзамен: {P_A:.4f}")
print(f"Вероятность того, что студент не сдал только первый экзамен, а второй сдал: {P_B_given_A:.4f}")

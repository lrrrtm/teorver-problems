def second_red_ball_probability(N, k, n):
    # Этап 1: Находим математическое ожидание числа красных шаров при условии, что один уже выбран и оказался красным
    E_R_given_A = 1 + (k - 1) * (n - 1) / (N - 1)

    # Этап 2: Вероятность того, что второй выбранный шар также будет красным
    P_second_red = E_R_given_A / n

    return P_second_red

# Пример ввода данных
N = int(input("Введите общее количество шаров N: "))
k = int(input("Введите количество красных шаров k: "))
n = int(input("Введите количество выбираемых шаров n: "))

# Вывод результата
result = second_red_ball_probability(N, k, n)
print(f"Вероятность того, что второй выбранный шар окажется красным, составляет приблизительно {result:.5f}")
